class ResNetDC(nn.Module):

    def __init__(
            self, features, num_classes, 
            sobel, with_pool=True, with_class=True,
            out_dim=512,
            ):
        super(ResNetDC, self).__init__()
        self.features = features
        if with_pool and with_class:
            self.classifier = nn.Sequential(
                nn.Linear(out_dim, out_dim),
                nn.ReLU(True),
                nn.Dropout(0.5),
                nn.Linear(out_dim, out_dim),
                nn.ReLU(True)
            )
            self.top_layer = nn.Linear(out_dim, num_classes)
        elif with_class:
            self.classifier = nn.Sequential(
                nn.Linear(out_dim * 7 * 7, 4096),
                nn.ReLU(True),
                nn.Dropout(0.5),
                nn.Linear(4096, 4096),
                nn.ReLU(True)
            )
            self.top_layer = nn.Linear(4096, num_classes)
        else:
            self.classifier = nn.Sequential(
                nn.ReLU(True),
            )
            self.top_layer = nn.Linear(out_dim, num_classes)
        self._initialize_weights()
        if sobel:
            grayscale = nn.Conv2d(3, 1, kernel_size=1, stride=1, padding=0)
            grayscale.weight.data.fill_(1.0 / 3.0)
            grayscale.bias.data.zero_()
            sobel_filter = nn.Conv2d(1, 2, kernel_size=3, stride=1, padding=1)
            sobel_filter.weight.data[0,0].copy_(
                torch.FloatTensor([[1, 0, -1], [2, 0, -2], [1, 0, -1]])
            )
            sobel_filter.weight.data[1,0].copy_(
                torch.FloatTensor([[1, 2, 1], [0, 0, 0], [-1, -2, -1]])
            )
            sobel_filter.bias.data.zero_()
            self.sobel = nn.Sequential(grayscale, sobel_filter)
            for p in self.sobel.parameters():
                p.requires_grad = False
        else:
            self.sobel = None

    def forward(self, x):
        if self.sobel:
            x = self.sobel(x)
        x = self.features(x)
        x = x.view(x.size(0), -1)
        x = self.classifier(x)
        if self.top_layer:
            x = self.top_layer(x)
        return x

    def _initialize_weights(self):
        for y,m in enumerate(self.modules()):
            if isinstance(m, nn.Conv2d):
                #print(y)
                n = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
                for i in range(m.out_channels):
                    m.weight.data[i].normal_(0, math.sqrt(2. / n))
                if m.bias is not None:
                    m.bias.data.zero_()
            elif isinstance(m, nn.BatchNorm2d):
                m.weight.data.fill_(1)
                m.bias.data.zero_()
            elif isinstance(m, nn.Linear):
                m.weight.data.normal_(0, 0.01)
                m.bias.data.zero_()


def resnet18_dc(sobel=False, out=1000):
    model = ResNetDC(resnet18(sobel=sobel), out, sobel)
    return model

